// перебераем массив. 1метод обычный цикл

for (let i = 0; i < arr.length; i++) { // будет отрабатывать все элементы внутри массива
    console.log(arr[i]);

}
// 2метод конструкция перебора for of. Плюс этого метода позволяет использовать команды брейк (прервать) и контининум (продолжить с какого то элемента)
for (let value of arr) {
    console.log(value);

}

// метод который больше всего используется в массиве forEach, который позволяет гибко перебрать все элементы которые лежат внутри массива. Он принимает внутрь себя еще одну функцию. В ней может содержаться три аргумента. первый значение, второй номер попорядку, третий это тот массив который перебираем

const arr2 = [10, 1, 30, 5, 7];
