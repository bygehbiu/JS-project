// операция split. Формирует массив из наименования строк. в сплит указывается разделить через который будут записываться товары в стороке

const str = prompt('', '');
const products = str.split(', ');
products.sort(); // метод сортировки. Отсортировалось в алфавитном порядке
console.log(products.join('; ')); // join объединение элементов массива в строку разделив значения ;